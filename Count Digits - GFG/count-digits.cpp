class Solution{
public:
    int evenlyDivides(int N){
        //code here
        int v = N;
        int count = 0;
        while(v>0)
        {
            int digits = v%10;
            
            if(digits!=0 && N%digits==0)
            {
                count++;
            }
            v=v/10;
        }
        return count;
    }
};

